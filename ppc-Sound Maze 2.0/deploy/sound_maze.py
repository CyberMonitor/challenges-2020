def adjacencia(v, n, m):
    adj = []
    if v[0] > 1:
        adj += [(v[0] - 1, v[1])]
    if v[1] > 1:
        adj += [(v[0], v[1] - 1)]
    if v[0] < n:
        adj += [(v[0] + 1, v[1])]
    if v[1] < m:
        adj += [(v[0], v[1] + 1)]
    return adj


def isend(tab, pos):
    return tab[pos[0]][pos[1]] == 'F'


def bfs(tab, ini):
    if isend(tab, ini):
        return 0
    fila = Queue()
    fila.put(ini)
    n = len(tab)
    m = len(tab[0])
    dist = np.array(n * [m * [-1]])
    dist[ini[0]][ini[1]] = 0
    while not fila.empty():
        v = fila.get()
        adj = adjacencia(v, n, m)
        for w in adj:
            if dist[w[0]][w[1]] == -1:
                dist[w[0]][w[1]] = dist[v[0]][v[1]] + 1
                if isend(tab, w):
                    return dist[w[0]][w[1]]
                if tab[w[0]][w[1]] == '.':
                    fila.put(w)
    return -1


